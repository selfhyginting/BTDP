public class OperatorPembanding{
	public static void main(String args[]) {
		int nilaiA=12;
		int nilaiB=4;
		boolean hasil;
		
		hasil = nilaiA>nilaiB;
		System.out.println(hasil);
		
		hasil = nilaiA<nilaiB;
		System.out.println(hasil);
		
		hasil = nilaiA>=nilaiB;
		System.out.println(hasil);

		hasil = nilaiA<=nilaiB;
		System.out.println(hasil);
		
		hasil = nilaiA==nilaiB;
		System.out.println(hasil);

		hasil = nilaiA != nilaiB;
		System.out.println(hasil);
	}
}